const express = require('express');
const auth = require('../../middlewares/auth');
const validate = require('../../middlewares/validate');
const botValidation = require('../../validations/bot.validation');
const botController = require('../../controllers/bot.controller');

const router = express.Router();

router
  .route('/')
  .post(auth('manageUsers'), validate(botValidation.createBot), botController.createBot)
  .get(auth('getUsers'), validate(botValidation.getBots), botController.getBots);

router
  .route('/:botId')
  .get(auth('getUsers'), validate(botValidation.getBot), botController.getBot)
  .patch(auth('manageUsers'), validate(botValidation.updateBot), botController.updateBot)
  .delete(auth('manageUsers'), validate(botValidation.deleteBot), botController.deleteBot);

router.route('/:botId/run').post(validate(botValidation.runBot), botController.runBot);
router.route('/:botId/expire').post(validate(botValidation.expireBot), botController.expireBot);

module.exports = router;

